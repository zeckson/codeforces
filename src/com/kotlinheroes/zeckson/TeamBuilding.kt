import kotlin.math.abs

fun main() {
    val scanner = Scanner(System.`in`)
    val students = scanner.nextInt()

    val levels = Array<Int>(students) { scanner.nextInt() }
    levels.sort()

    var tasks = 0;
    for (i in 0 until students / 2) {
        tasks += abs(levels[i * 2] - levels[i * 2 + 1])
    }

    println(tasks)
}
